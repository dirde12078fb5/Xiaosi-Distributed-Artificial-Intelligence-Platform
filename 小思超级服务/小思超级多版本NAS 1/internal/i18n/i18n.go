package i18n

type Manager struct {
	supported    []string
	defaultLang  string
	translations map[string]map[string]string
}

func NewManager(supported []string, defaultLang string) *Manager {
	m := &Manager{
		supported:    supported,
		defaultLang:  defaultLang,
		translations: make(map[string]map[string]string),
	}
	m.loadTranslations()
	return m
}

