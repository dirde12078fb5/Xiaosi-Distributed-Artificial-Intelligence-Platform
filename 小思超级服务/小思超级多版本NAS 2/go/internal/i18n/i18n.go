func (m *Manager) GetTranslations(lang string) map[string]string {
	if trans, ok := m.translations[lang]; ok {
		return trans
	}
	return m.translations[m.defaultLang]
}

func (m *Manager) GetSupportedLanguages() []string {
	return m.supported
}

func (m *Manager) Translate(lang, key string) string {
	trans := m.GetTranslations(lang)
	if val, ok := trans[key]; ok {
		return val
	}
	return key
}