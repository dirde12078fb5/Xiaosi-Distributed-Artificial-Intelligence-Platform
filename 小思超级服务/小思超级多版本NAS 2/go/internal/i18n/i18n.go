func NewManager(cfg *config.I18nConfig) *Manager {
	m := &Manager{
		supported:    cfg.Supported,
		defaultLang:  cfg.Default,
		translations: make(map[string]map[string]string),
	}
	m.loadAllTranslations()
	return m
}

