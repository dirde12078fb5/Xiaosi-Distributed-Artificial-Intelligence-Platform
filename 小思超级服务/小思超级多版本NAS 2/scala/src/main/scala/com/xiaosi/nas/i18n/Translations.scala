  def get(lang: LangCode, key: TranslationKey): Translation = {
    translations.get(lang).flatMap(_.get(key)).getOrElse(key)
  }
  
  // 获取所有语言代码
  def supportedLanguages: List[LangCode] = translations.keys.toList
  
  // 获取特定语言的完整翻译
  def getTranslations(lang: LangCode): Map[TranslationKey, Translation] = {
    translations.getOrElse(lang, translations("en_US"))
  }
